public class loops{
    public static void main(String argc[])
    {
        int j=0;
        while(j<6)
        {
            System.out.println("hii"+ j);
            j++;
        }

        for(int i=0; i<5; i++)
        {
            System.out.println("hello");
        }

        int k=0;
        do{
            System.out.println("hii"+" "+k);
            k++;
        }while(k<4);
    }
}